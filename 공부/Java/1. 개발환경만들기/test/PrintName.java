package test;
//String 변수 name에 “홍길동”이라는 리터럴 데이터를 넣어서 콘솔에 출력하는 프로그램을 작성하시오.
//클래스 이름은 test.PrintName이라는 클래스의 main()에 작성하시오.
public class PrintName {

	public static void main(String[] args) {

		// 문자열 변수 name을 선언하고 "홍길동" 리터럴 데이터 초기화
		// 리터럴(Literal): 정수, 실수, 문자, 문자열, 논리 값 등을 의미
		// "홍길동": 문자열 리터럴
		String name = "홍길동";

		// 변수 name의 값을 콘솔에 출력
		System.out.println(name);
	}
}
